# Função para calcular o tempo p/ tomar distancia do outro carro
def getDistancia(dist):
    return dist * 2

# Recebendo Valores
distancia = int(input())

# Calculando a Distância
result = getDistancia(distancia)

# Imprimindo o resultado
print(f"{result} minutos")

